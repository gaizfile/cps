package core.classes;

public class StatWard {

	private int ward_stat_ID;
	private Hospital hospital;
	private int ward_year;
	private String ward_no;
	private String ward_category;
	private char ward_gender;
	private int ward_beds;
	
	public int getWard_stat_ID() {
		return ward_stat_ID;
	}
	public void setWard_stat_ID(int ward_stat_ID) {
		this.ward_stat_ID = ward_stat_ID;
	}
	public Hospital getHospital() {
		return hospital;
	}
	public void setHospital(Hospital hospital) {
		this.hospital = hospital;
	}
	public int getWard_year() {
		return ward_year;
	}
	public void setWard_year(int ward_year) {
		this.ward_year = ward_year;
	}
	public String getWard_no() {
		return ward_no;
	}
	public void setWard_no(String ward_no) {
		this.ward_no = ward_no;
	}
	public String getWard_category() {
		return ward_category;
	}
	public void setWard_category(String ward_category) {
		this.ward_category = ward_category;
	}
	public char getWard_gender() {
		return ward_gender;
	}
	public void setWard_gender(char ward_gender) {
		this.ward_gender = ward_gender;
	}
	public int getWard_beds() {
		return ward_beds;
	}
	public void setWard_beds(int ward_beds) {
		this.ward_beds = ward_beds;
	}


	
}
