package core.classes;

public class Hospital {
	
private String hospital_ID;
private String hospital_IP;
private int hospital_Port;
private String hospital_Name;
private String hospital_District;
private String hospital_Province;



public String getHospital_ID() {
	return hospital_ID;
}
public void setHospital_ID(String hospital_ID) {
	this.hospital_ID = hospital_ID;
}
public String getHospital_IP() {
	return hospital_IP;
}
public void setHospital_IP(String hospital_IP) {
	this.hospital_IP = hospital_IP;
}
public int getHospital_Port() {
	return hospital_Port;
}
public void setHospital_Port(int hospital_Port) {
	this.hospital_Port = hospital_Port;
}
public String getHospital_Name() {
	return hospital_Name;
}
public void setHospital_Name(String hospital_Name) {
	this.hospital_Name = hospital_Name;
}
public String getHospital_District() {
	return hospital_District;
}
public void setHospital_District(String hospital_District) {
	this.hospital_District = hospital_District;
}
public String getHospital_Province() {
	return hospital_Province;
}
public void setHospital_Province(String hospital_Province) {
	this.hospital_Province = hospital_Province;
}
}
